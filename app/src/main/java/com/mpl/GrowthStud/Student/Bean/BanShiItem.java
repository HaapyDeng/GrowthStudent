package com.mpl.GrowthStud.Student.Bean;

public class BanShiItem {
    private String imge;
    private String id;

    public BanShiItem(String imge, String id) {
        this.imge = imge;
        this.id = id;
    }

    public String getImge() {
        return imge;
    }

    public void setImge(String imge) {
        this.imge = imge;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }


}
